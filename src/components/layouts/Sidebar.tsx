export default function Sidebar({ isOpen, onClose }: SidebarProps) {
  const pathname = usePathname();
  const [expandedItems, setExpandedItems] = useState<string[]>([]);
  const [searchQuery, setSearchQuery] = useState('');
  const [isMobile, setIsMobile] = useState(false);
  const touchStartX = useRef<number>(0);
  const touchEndX = useRef<number>(0);

  // Check if device is mobile
  useEffect(() => {
    const checkIsMobile = () => {
      setIsMobile(window.innerWidth < 768); // md breakpoint
    };

    checkIsMobile();
    window.addEventListener('resize', checkIsMobile);

    return () => window.removeEventListener('resize', checkIsMobile);
  }, []);

  // Swipe gesture handlers for sidebar
  const handleSidebarTouchStart = (e: React.TouchEvent) => {
    if (!isMobile) return;
    touchStartX.current = e.targetTouches[0].clientX;
  };

  const handleSidebarTouchMove = (e: React.TouchEvent) => {
    if (!isMobile) return;
    touchEndX.current = e.targetTouches[0].clientX;
  };

  const handleSidebarTouchEnd = () => {
    if (!isMobile) return;
    
    const swipeThreshold = 50; // Minimum distance for a swipe
    const swipeDistance = touchStartX.current - touchEndX.current;

    // Swipe right to close sidebar
    if (swipeDistance > swipeThreshold) {
      onClose();
    }
  };

  const toggleExpanded = (title: string) => {
    setExpandedItems(prev =>
      prev.includes(title)
        ? prev.filter(item => item !== title)
        : [...prev, title]
    );
  };

  const isActive = (href: string) => {
    return pathname === href;
  };

  // Filter menu items based on search query
  const filteredMenuItems = menuItems.filter(item => {
    if (!searchQuery.trim()) return true;
    
    const matchesMainItem = item.title.toLowerCase().includes(searchQuery.toLowerCase());
    const matchesChildren = item.children?.some(child => 
      child.title.toLowerCase().includes(searchQuery.toLowerCase())
    );
    
    return matchesMainItem || matchesChildren;
  });

  // Auto-expand items that have matching children
  const getExpandedItems = () => {
    if (!searchQuery.trim()) return expandedItems;
    
    const itemsToExpand = menuItems
      .filter(item => 
        item.children?.some(child => 
          child.title.toLowerCase().includes(searchQuery.toLowerCase())
        )
      )
      .map(item => item.title);
    
    return [...new Set([...expandedItems, ...itemsToExpand])];
  };

  const renderMenuItem = (item: MenuItem, level: number = 0, isLastChild: boolean = false, hasParent: boolean = false) => {
    const hasChildren = item.children && item.children.length > 0;
    const isExpanded = getExpandedItems().includes(item.title);
    const active = isActive(item.href);
    const isSearching = searchQuery.trim().length > 0;
    const isMatched = isSearching && (
      item.title.toLowerCase().includes(searchQuery.toLowerCase()) ||
      item.children?.some(child => child.title.toLowerCase().includes(searchQuery.toLowerCase()))
    );

    return (
      <div key={item.title} className="relative">
        
        {hasChildren ? (
          // Parent menu with children - not a link, just for expanding/collapsing
          <button
            onClick={(e) => {
              e.preventDefault();
              e.stopPropagation();
              toggleExpanded(item.title);
            }}
            onTouchStart={(e) => {
              e.stopPropagation();
            }}
            onTouchEnd={(e) => {
              e.stopPropagation();
            }}
            className={`
              w-full flex items-center gap-3 px-4 py-3 text-sm font-medium rounded-lg transition-all duration-200 ease-in-out relative
              ${level === 0 ? 'mx-2 mb-1' : 'mr-8 ml-6'}
              ${
                isMatched
                  ? 'bg-yellow-50 dark:bg-yellow-900/20 text-gray-900 dark:text-gray-100 hover:bg-yellow-100 dark:hover:bg-yellow-900/30 hover:shadow-sm'
                  : 'text-gray-700 dark:text-gray-300 hover:bg-gray-100 dark:hover:bg-gray-800 hover:shadow-sm'
              }
            `}
          >
            
            <span>{item.icon}</span>
            <span className="flex-1 text-right">{item.title}</span>
            <svg
              className={`w-4 h-4 transition-transform duration-300 ease-in-out ${
                isExpanded ? 'rotate-180' : ''
              }`}
              fill="none"
              stroke="currentColor"
              viewBox="0 0 24 24"
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth={2}
                d="M19 9l-7 7-7-7"
              />
            </svg>
          </button>
        ) : (
          // Child menu or menu without children - regular link
          <Link
            href={item.href}
            className={`
              flex items-center gap-3 px-4 py-3 text-sm font-medium rounded-lg transition-all duration-200 ease-in-out relative
              ${level === 0 ? 'mx-2 mb-1' : 'mr-8 ml-6'}
              ${
                active
                  ? 'bg-blue-100 dark:bg-blue-900/30 text-blue-700 dark:text-blue-300 shadow-sm'
                  : isMatched
                  ? 'bg-yellow-50 dark:bg-yellow-900/20 text-gray-900 dark:text-gray-100 hover:bg-yellow-100 dark:hover:bg-yellow-900/30 hover:shadow-sm'
                  : 'text-gray-700 dark:text-gray-300 hover:bg-gray-100 dark:hover:bg-gray-800 hover:shadow-sm'
              }
            `}
            onClick={(e) => {
              // Only close sidebar on mobile for menus without children
              if (isMobile) {
                e.preventDefault();
                e.stopPropagation();
                onClose();
              }
            }}
            onTouchStart={(e) => {
              e.stopPropagation();
            }}
            onTouchEnd={(e) => {
              e.stopPropagation();
            }}
          >
            
            <span className={`${active ? 'text-blue-600' : ''}`}>
              {item.icon}
            </span>
            <span className="flex-1">{item.title}</span>
          </Link>
        )}

        {hasChildren && (
          <div 
            className={`overflow-hidden transition-all duration-300 ease-in-out ${
              isExpanded ? 'max-h-96 opacity-100' : 'max-h-0 opacity-0'
            }`}
          >
            <div className="mt-1 space-y-1 relative">
              {item.children!.map((child, index) => 
                renderMenuItem(
                  child, 
                  level + 1, 
                  index === item.children!.length - 1,
                  true
                )
              )}
            </div>
          </div>
        )}
      </div>
    );
  };

  return (
    <>
      {/* Mobile overlay */}
      {isOpen && (
        <div
          className="fixed inset-0 bg-black bg-opacity-50 z-40 md:hidden"
          onClick={onClose}
        />
      )}

      {/* Sidebar */}
      <aside
        className={`
          w-64 bg-white dark:bg-gray-900 shadow-lg transform transition-transform duration-300 ease-in-out
          ${isOpen ? 'translate-x-0' : 'translate-x-full'}
          fixed inset-y-0 right-0 z-50 md:translate-x-0
        `}
        onTouchStart={(e) => {
          e.stopPropagation();
          handleSidebarTouchStart(e);
        }}
        onTouchMove={(e) => {
          e.stopPropagation();
          handleSidebarTouchMove(e);
        }}
        onTouchEnd={(e) => {
          e.stopPropagation();
          handleSidebarTouchEnd();
        }}
        onClick={(e) => {
          e.stopPropagation();
        }}
      >
        <div className="flex flex-col h-full">
          {/* Sidebar header */}
          <div className="relative p-4 border-b border-gray-200 dark:border-gray-700">
            <div className="flex items-center justify-center w-full h-16">
              <Image
                src="/images/logo.png"
                alt="Logo"
                width={120}
                height={64}
                className="rounded-lg object-contain w-full h-full"
              />
            </div>
            <button
              onClick={onClose}
              className="absolute top-2 left-2 md:hidden p-2 hover:bg-gray-100 dark:hover:bg-gray-800 rounded-lg"
            >
              <svg
                className="w-5 h-5"
                fill="none"
                stroke="currentColor"
                viewBox="0 0 24 24"
              >
                <path
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  strokeWidth={2}
                  d="M6 18L18 6M6 6l12 12"
                />
              </svg>
            </button>
          </div>

          {/* Search bar */}
          <div className="p-4 border-b border-gray-200">
            <div className="relative">
              <input
                type="text"
                placeholder="جستجو در منو..."
                value={searchQuery}
                onChange={(e) => setSearchQuery(e.target.value)}
                className="w-full px-4 py-2 pr-10 text-sm bg-gray-100 dark:bg-gray-800 border border-gray-200 dark:border-gray-700 text-gray-900 dark:text-gray-100 rounded-lg focus:outline-none focus:ring-2 focus:ring-blue-500 focus:border-transparent"
              />
              <div className="absolute inset-y-0 right-0 flex items-center pr-3">
                <svg
                  className="w-4 h-4 text-gray-400"
                  fill="none"
                  stroke="currentColor"
                  viewBox="0 0 24 24"
                >
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    strokeWidth={2}
                    d="M21 21l-6-6m2-5a7 7 0 11-14 0 7 7 0 0114 0z"
                  />
                </svg>
              </div>
              {searchQuery && (
                <button
                  onClick={() => setSearchQuery('')}
                  className="absolute inset-y-0 left-0 flex items-center pl-3"
                >
                  <svg
                    className="w-4 h-4 text-gray-400 hover:text-gray-600"
                    fill="none"
                    stroke="currentColor"
                    viewBox="0 0 24 24"
                  >
                    <path
                      strokeLinecap="round"
                      strokeLinejoin="round"
                      strokeWidth={2}
                      d="M6 18L18 6M6 6l12 12"
                    />
                  </svg>
                </button>
              )}
            </div>
          </div>

          {/* Navigation menu */}
          <nav className="flex-1 overflow-y-auto py-4 scrollbar-thin">
            <div className="space-y-1 px-2">
              {filteredMenuItems.length > 0 ? (
                filteredMenuItems.map(item => renderMenuItem(item))
              ) : (
                <div className="px-4 py-8 text-center">
                  <svg
                    className="w-12 h-12 text-gray-400 mx-auto mb-4"
                    fill="none"
                    stroke="currentColor"
                    viewBox="0 0 24 24"
                  >
                    <path
                      strokeLinecap="round"
                      strokeLinejoin="round"
                      strokeWidth={2}
                      d="M21 21l-6-6m2-5a7 7 0 11-14 0 7 7 0 0114 0z"
                    />
                  </svg>
                  <p className="text-gray-500 dark:text-gray-400 text-sm">
                    موردی یافت نشد
                  </p>
                </div>
              )}
            </div>
          </nav>

          {/* Sidebar footer */}
          <div className="p-4 border-t border-gray-200 dark:border-gray-700">
            <div className="flex items-center gap-3 p-3 bg-gray-50 dark:bg-gray-800 rounded-lg">
              <div className="w-10 h-10 bg-blue-500 rounded-full flex items-center justify-center">
                <span className="text-white font-medium">ا</span>
              </div>
              <div className="flex-1 min-w-0">
                <p className="text-sm font-medium text-gray-900 dark:text-gray-100 truncate">
                  ادمین سیستم
                </p>
                <p className="text-xs text-gray-500 dark:text-gray-400 truncate">
                  admin@example.com
                </p>
              </div>
            </div>
          </div>
        </div>
      </aside>
    </>
  );
}
